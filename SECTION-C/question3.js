//Create a function that takes two numbers.
//  If the numbers are the same, return their product. 
// If they are different, return their sum. 

function calculate(num1, num2) {
    if (num1 === num2) {
        return num1 * num2;
    } else {
        return num1 + num2;
    }
}
console.log(calculate(7, 4)); 
console.log(calculate(7, 4)); 
